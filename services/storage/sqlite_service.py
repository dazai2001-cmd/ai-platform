class SQLiteService(DatabaseJSONMixin):
    backend = "sqlite"

    def __init__(self, path: str | None = None):
        self.path = Path(path or settings.SQLITE_PATH)
        self.path.parent.mkdir(parents=True, exist_ok=True)
        self._lock = threading.Lock()
        self._init()

    def connect(self):
        conn = sqlite3.connect(self.path, check_same_thread=False, timeout=10)
        conn.row_factory = sqlite3.Row
        conn.execute("PRAGMA foreign_keys = ON")
        conn.execute("PRAGMA busy_timeout = 10000")
        conn.execute("PRAGMA synchronous = NORMAL")
        return conn

    def execute(self, sql: str, params: tuple = ()):
        with self._lock, self.connect() as conn:
            conn.execute(sql, params)
            conn.commit()

    def query(self, sql: str, params: tuple = ()) -> list[dict[str, Any]]:
        with self.connect() as conn:
            rows = conn.execute(sql, params).fetchall()
            return [dict(row) for row in rows]

    def query_one(self, sql: str, params: tuple = ()) -> dict[str, Any] | None:
        with self.connect() as conn:
            row = conn.execute(sql, params).fetchone()
            return dict(row) if row else None

    def execute_many(self, statements: list[tuple[str, tuple]]):
        with self._lock, self.connect() as conn:
            for sql, params in statements:
                conn.execute(sql, params)
            conn.commit()

    def _init(self):
        with self.connect() as conn:
            conn.execute("PRAGMA journal_mode = WAL")
        self.execute(
            f"""
            CREATE TABLE IF NOT EXISTS {SCHEMA_MIGRATIONS_TABLE} (
              version INTEGER PRIMARY KEY,
              name TEXT NOT NULL,
              applied_at DOUBLE PRECISION NOT NULL
            )
            """
        )
        applied = {
            int(row["version"])
            for row in self.query(f"SELECT version FROM {SCHEMA_MIGRATIONS_TABLE}")
        }
        if 1 not in applied:
            statements = [(sql, ()) for sql in _schema_statements(self.backend)]
            statements.append((
                f"INSERT INTO {SCHEMA_MIGRATIONS_TABLE} (version, name, applied_at) VALUES (?, ?, ?)",
                (1, "initial_schema", time.time()),
            ))
            self.execute_many(statements)
        if 2 not in applied:
            self._migrate_user_scopes()
            self.execute(
                f"INSERT INTO {SCHEMA_MIGRATIONS_TABLE} (version, name, applied_at) VALUES (?, ?, ?)",
                (2, "user_scopes", time.time()),
            )
        if 3 not in applied:
            self.execute_many([(sql, ()) for sql in _performance_index_statements()])
            self.execute(
                f"INSERT INTO {SCHEMA_MIGRATIONS_TABLE} (version, name, applied_at) VALUES (?, ?, ?)",
                (3, "foreign_key_indexes", time.time()),
            )

    def _has_column(self, table: str, column: str) -> bool:
        rows = self.query(f"PRAGMA table_info({table})")
        return any(row["name"] == column for row in rows)

    def _add_column_if_missing(self, table: str, column: str, ddl: str):
        if not self._has_column(table, column):
            self.execute(f"ALTER TABLE {table} ADD COLUMN {column} {ddl}")

    def _migrate_user_scopes(self):
        self._migrate_career_singleton_table(
            "career_preferences", "preferences_json TEXT NOT NULL", "preferences_json", "{}"
        )
        self._migrate_career_singleton_table(
            "career_profile", "cv_text TEXT NOT NULL", "cv_text", ""
        )
        self._add_column_if_missing("memory_messages", "user_id", "TEXT NOT NULL DEFAULT 'local'")
        self._add_column_if_missing("memory_facts", "user_id", "TEXT NOT NULL DEFAULT 'local'")
        self._add_column_if_missing("chat_conversations", "user_id", "TEXT NOT NULL DEFAULT 'local'")
        self._add_column_if_missing("career_jobs", "user_id", "TEXT NOT NULL DEFAULT 'local'")
        self._add_column_if_missing("career_jobs", "applied_at", "DOUBLE PRECISION")
        self._add_column_if_missing("career_score_batches", "user_id", "TEXT NOT NULL DEFAULT 'local'")
        self.execute_many([(sql, ()) for sql in _scope_index_statements()])

    def _migrate_career_singleton_table(
        self,
        table: str,
        value_ddl: str,
        value_column: str,
        fallback: str,
    ):
        columns = [row["name"] for row in self.query(f"PRAGMA table_info({table})")]
        if not columns or "id" not in columns:
            return

        row = self.query_one(f"SELECT {value_column}, updated_at FROM {table} WHERE id = 1")
        value = row[value_column] if row else fallback
        updated_at = row["updated_at"] if row else 0
        self.execute_many([
            (f"DROP TABLE {table}", ()),
            (
                f"""
                CREATE TABLE {table} (
                  user_id TEXT PRIMARY KEY,
                  {value_ddl},
                  updated_at DOUBLE PRECISION NOT NULL
                )
                """,
                (),
            ),
            (
                f"INSERT INTO {table} (user_id, {value_column}, updated_at) VALUES (?, ?, ?)",
                ("local", value, updated_at),
            ),
        ])


class PostgreSQLService(DatabaseJSONMixin):
    backend = "postgresql"

    def __init__(
        self,
        database_url: str,
        *,
        pool: Any | None = None,
        initialize: bool = True,
        schema: str | None = None,
    ):
        self.database_url = (database_url or "").strip()
        parsed = urlsplit(self.database_url)
        if parsed.scheme.lower() not in POSTGRES_SCHEMES or not parsed.hostname:
            raise ValueError("DATABASE_URL must be a PostgreSQL connection URL")

        self.schema = (schema or settings.DATABASE_SCHEMA).strip()
        if not SCHEMA_NAME_PATTERN.fullmatch(self.schema):
            raise ValueError("DATABASE_SCHEMA must be a simple PostgreSQL identifier")

        self._owns_pool = pool is None
        self._closed = False
        self._pool = pool or self._create_pool()
        if self._owns_pool:
            self._pool.wait(timeout=settings.DATABASE_CONNECT_TIMEOUT_SECONDS)
            atexit.register(self.close)
        if initialize:
            self._init()

    def _create_pool(self):
        try:
            from psycopg.rows import dict_row
            from psycopg_pool import ConnectionPool
        except ImportError as exc:  # pragma: no cover - exercised in deployment packaging
            raise RuntimeError(
                "PostgreSQL support requires the psycopg and psycopg-pool packages"
            ) from exc

        connection_kwargs: dict[str, Any] = {
            "application_name": "ai-platform",
            "connect_timeout": settings.DATABASE_CONNECT_TIMEOUT_SECONDS,
            "prepare_threshold": None,
            "row_factory": dict_row,
        }
        if settings.DATABASE_SSLMODE:
            connection_kwargs["sslmode"] = settings.DATABASE_SSLMODE
        return ConnectionPool(
            conninfo=self.database_url,
            min_size=settings.DATABASE_POOL_MIN_SIZE,
            max_size=settings.DATABASE_POOL_MAX_SIZE,
            timeout=settings.DATABASE_CONNECT_TIMEOUT_SECONDS,
            kwargs=connection_kwargs,
            check=ConnectionPool.check_connection,
            open=True,
        )

    @contextmanager
    def connect(self) -> Iterator[Any]:
        with self._pool.connection() as conn:
            # SET LOCAL is transaction-scoped, so it is safe with both a local
            # client pool and Supabase's transaction-mode pooler.
            conn.execute(f'SET LOCAL search_path TO "{self.schema}"')
            yield conn

    def close(self):
        if self._owns_pool and self._pool is not None and not self._closed:
            self._pool.close()
            self._closed = True

    def execute(self, sql: str, params: tuple = ()):
        with self.connect() as conn:
            conn.execute(self._convert_placeholders(sql), params)

    def query(self, sql: str, params: tuple = ()) -> list[dict[str, Any]]:
        with self.connect() as conn:
            rows = conn.execute(self._convert_placeholders(sql), params).fetchall()
            return [dict(row) for row in rows]

    def query_one(self, sql: str, params: tuple = ()) -> dict[str, Any] | None:
        with self.connect() as conn:
            row = conn.execute(self._convert_placeholders(sql), params).fetchone()
            return dict(row) if row else None

    def execute_many(self, statements: list[tuple[str, tuple]]):
        with self.connect() as conn:
            for sql, params in statements:
                conn.execute(self._convert_placeholders(sql), params)

    def _init(self):
        with self.connect() as conn:
            conn.execute("SELECT pg_advisory_xact_lock(%s)", (POSTGRES_MIGRATION_LOCK_ID,))
            conn.execute(f'CREATE SCHEMA IF NOT EXISTS "{self.schema}"')
            conn.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {SCHEMA_MIGRATIONS_TABLE} (
                  version INTEGER PRIMARY KEY,
                  name TEXT NOT NULL,
                  applied_at DOUBLE PRECISION NOT NULL
                )
                """
            )
            applied = {
                int(row["version"])
                for row in conn.execute(
                    f"SELECT version FROM {SCHEMA_MIGRATIONS_TABLE}"
                ).fetchall()
            }
            if 1 not in applied:
                for statement in _schema_statements(self.backend):
                    conn.execute(statement)
                conn.execute(
                    f"INSERT INTO {SCHEMA_MIGRATIONS_TABLE} "
                    "(version, name, applied_at) VALUES (%s, %s, %s)",
                    (1, "initial_schema", time.time()),
                )
            if 2 not in applied:
                for statement in self._postgres_scope_migration_statements():
                    conn.execute(statement)
                conn.execute(
                    f"INSERT INTO {SCHEMA_MIGRATIONS_TABLE} "
                    "(version, name, applied_at) VALUES (%s, %s, %s)",
                    (2, "user_scopes", time.time()),
                )
            if 3 not in applied:
                for statement in _performance_index_statements():
                    conn.execute(statement)
                conn.execute(
                    f"INSERT INTO {SCHEMA_MIGRATIONS_TABLE} "
                    "(version, name, applied_at) VALUES (%s, %s, %s)",
                    (3, "foreign_key_indexes", time.time()),
                )

    @staticmethod
    def _postgres_scope_migration_statements() -> list[str]:
        return [
            "ALTER TABLE memory_messages ADD COLUMN IF NOT EXISTS user_id TEXT NOT NULL DEFAULT 'local'",
            "ALTER TABLE memory_facts ADD COLUMN IF NOT EXISTS user_id TEXT NOT NULL DEFAULT 'local'",
            "ALTER TABLE chat_conversations ADD COLUMN IF NOT EXISTS user_id TEXT NOT NULL DEFAULT 'local'",
            "ALTER TABLE career_jobs ADD COLUMN IF NOT EXISTS user_id TEXT NOT NULL DEFAULT 'local'",
            "ALTER TABLE career_jobs ADD COLUMN IF NOT EXISTS applied_at DOUBLE PRECISION",
            "ALTER TABLE career_score_batches ADD COLUMN IF NOT EXISTS user_id TEXT NOT NULL DEFAULT 'local'",
            *_scope_index_statements(),
        ]

    @staticmethod
    def _convert_placeholders(sql: str) -> str:
        """Convert DB-API qmark placeholders without touching SQL literals.

        Existing application services intentionally keep the SQLite-style
        ``?`` API. Psycopg uses ``%s``; this small lexer handles quoted text,
        identifiers, comments, and PostgreSQL dollar-quoted strings.
        """

        output: list[str] = []
        index = 0
        state = "normal"
        dollar_tag = ""
        while index < len(sql):
            char = sql[index]
            next_char = sql[index + 1] if index + 1 < len(sql) else ""

            if state == "normal":
                if char == "'":
                    state = "single"
                elif char == '"':
                    state = "double"
                elif char == "-" and next_char == "-":
                    output.extend((char, next_char))
                    index += 2
                    state = "line_comment"
                    continue
                elif char == "/" and next_char == "*":
                    output.extend((char, next_char))
                    index += 2
                    state = "block_comment"
                    continue
                elif char == "$":
                    match = re.match(r"\$[A-Za-z_][A-Za-z0-9_]*\$|\$\$", sql[index:])
                    if match:
                        dollar_tag = match.group(0)
                        output.append(dollar_tag)
                        index += len(dollar_tag)
                        state = "dollar"
                        continue
                elif char == "?":
                    output.append("%s")
                    index += 1
                    continue
            elif state == "single":
                if char == "'":
                    if next_char == "'":
                        output.extend((char, next_char))
                        index += 2
                        continue
                    state = "normal"
            elif state == "double":
                if char == '"':
                    if next_char == '"':
                        output.extend((char, next_char))
                        index += 2
                        continue
                    state = "normal"
            elif state == "line_comment":
                if char in "\r\n":
                    state = "normal"
            elif state == "block_comment":
                if char == "*" and next_char == "/":
                    output.extend((char, next_char))
                    index += 2
                    state = "normal"
                    continue
            elif state == "dollar" and sql.startswith(dollar_tag, index):
                output.append(dollar_tag)
                index += len(dollar_tag)
                state = "normal"
                continue

            output.append(char)
            index += 1
        return "".join(output)


PostgresService = PostgreSQLService


def create_database_service(
    database_url: str | None = None,
    *,
    sqlite_path: str | None = None,
):
    resolved_url = settings.DATABASE_URL if database_url is None else database_url
    if not (resolved_url or "").strip():
        return SQLiteService(sqlite_path)
    return PostgreSQLService(resolved_url)


db = create_database_service()
